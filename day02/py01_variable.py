# 변수와 자료형 
# 변수는 => 변하는 수 (Variable) <-> 상수(Constant) 예 pi = 3.1415927...

# 변수 
a = None # None 아무것도 아닌 값, 알 수 없는 값
print(a) # a라는 변수에 무슨 값이 들어있는지 콘솔에 출력 
            #print() -> 출력 함수
        
print(type(a)) # a의 자료형을 출력


            
a = 10   # 정수형 Integer ( 소수점이 없는 수)
print(a) # 함수는 늘 괄호를 같이 사용
print(type(a)) # a의 자료형을 출력


a = 12.34  # 실수 Float( 소수점 아래의 수)
print(a)
print(type(a)) # a의 자료형을 출력


a = 0b11111110 # 2진수 Binary(0과 1로 수를 표현하는 방식)
print(a)
print(type(a)) # a의 자료형을 출력



a = 0xFE # 16진수 Hex (0 ~ 9, A, B, C, D, E, F[15] 로 수를 표현하는 방식)
print(a)
print(type(a)) # a의 자료형을 출력



a = 1_900_000_000 # 천 단위 쉼표 표현
print(a)
print(type(a)) # a의 자료형을 출력



a = "Life is short, You need Python" # 문자열 String
print(a)
print(type(a)) # a의 자료형을 출력



a = 'Life is short, You need Python!' # 문자열 
print(a)
print(type(a)) # a의 자료형을 출력



a = (3 > 1)
print(a) # boolean 형 (True | False)
print(type(a)) # a의 자료형을 출력






